<p>Information provided on the understanding that it will be shared with a third party</p>
<p>Our website allows you to post information with a view to that information being read, copied, downloaded, or used by other people.
<h4>Examples include:</h4>
<ul>
<li>posting a message our forum</li>
<li>tagging an image</li>
<li>clicking on an icon next to another visitor’s message to convey your agreement, disagreement or thanks</li>
<li>In posting personal information, it is up to you to satisfy yourself about the privacy level of every person who might use it.</li>
<li>We do not specifically use this information except to allow it to be displayed or shared.</li>
<li>We do store it, and we reserve a right to use it in the future in any way we decide.</li>
</ul>
<p> Once your information enters the public domain, we have no control over what any individual third party may do with it. We accept no 
responsibility for their actions at any time.Provided your request is reasonable and there is no legal basis for us to retain it, then at our discretion </p>
<p>we may agree to your request to delete personal information that you have posted. You can make a request by contacting us at 
<a target="_blank" href="readyfreddy.co.uk.">readyfreddy.co.uk.</a> </p>

<h4>Complaints regarding content on our website</h4>
<p>Our website is a publishing medium. Anyone may register and then publish information about himself, herself or some other person.
We attempt to moderate user generated content, but we are not always able to do so as soon as that content is published.</p>
<p>If you complain about any of the content on our website, we shall investigate your complaint.</p>
<p>If we feel it is justified or if we believe the law requires us to do so, we shall remove the content while we investigate.
Free speech is a fundamental right, so we have to make a judgment as to whose right will be obstructed: yours, or that of the person who 
