<p>This is the privacy notice of Ready Freddy Ltd. In this document, “we”, “our”, or “us” refer to  Ready Freddy Ltd.
We are company number 1188201 registered in United Kingdom.</p>
<p>Our registered office is at 79 Colwith Road, W6 9EZ.</p>

<h4>Introduction</h4>
<p>This privacy notice aims to inform you about how we collect and process any information that we collect from you, or that you provide to us. 
It covers information that could identify you (“personal information”) and information that could not. In the context of the law and this notice, 
“process” means collect, store, transfer, use or otherwise act on information. It tells you about your privacy rights and how the law protects 
you. We are committed to protecting your privacy and the confidentiality of your personal information. Our policy is not just an exercise in 
complying with the law,  but a continuation of our respect for you and your personal information. We undertake to preserve the confidentiality
of all information you  provide to us, and  hope that you reciprocate.</p>
<p>Our policy complies with the Data Protection Act 2018 (Act) accordingly incorporating the EU General Data Protection Regulation (GDPR).
The law requires us to tell you about your rights and our obligations to you in regards to the processing and control of your personal data. 
We do this now,  by requesting that you read the information provided at <a target="_blank" href="http://www.knowyourprivacyrights.org">http://www.knowyourprivacyrights.org</a> Except as set out below, we do not share, or sell, or disclose to a third party, any information collected through our website.</p>

<h4>Data we process</h4>
<p>We may collect, use, store and transfer different kinds of personal data about you. We have collated these into groups as follows:
Your identity includes information such as first name, last name, title, date of birth, and other identifiers that you may have provided at some 
time. Your contact information includes information such as billing address, delivery address, email address, telephone numbers and any other 
information you have given to us for the purpose of communication or meeting. Your financial data includes information such as your bank 
account and payment card details.</p>
<p>Transaction data includes details about payments or communications to and from you and information about products and services you have 
purchased from us.</p>
<p>Technical data includes you internet protocol (IP) address, browser type and version, time zone setting and location, browser plug-in types and 
versions, operating system and platform and other technology on the devices you use to access this website.</p>
<p>Your profile includes information such as your username and password, purchases or orders made by you, your interests, preferences, feedback 
and survey responses. Marketing data includes your preferences in receiving marketing from us; communication preferences; responses and
actions in relation to your use of our services. We may aggregate anonymous data such as statistical or demographic data for any purpose. 
Anonymous data is data that does  not identify you as an individual. Aggregated data may be derived from your personal data but is not 
considered personal information in law because it does not reveal your identity.</p>
<p>For example, we may aggregate profile data to assess interest in a product or service. However, if we combine or connect aggregated data 
with your personal information so that it can identify you in any way, we treat the combined data as personal information and it will be used in
accordance with this privacy notice.</p>

<h4>Special personal information</h4>
<p>Special personal information is data about your race or ethnicity, religious or philosophical beliefs, sex life, sexual orientation, political 
opinions, trade union membership, information about your health and genetic and biometric data.It also includes information about criminal 
convictions and offences.</p>
<p>We may collect special personal information about you if there is a lawful basis on which to do so.</p>

<h4>If you do not provide personal information we need</h4>
<p>Where we need to collect personal data by law, or under the terms of a contract we have with you, and you fail to provide that data when 
requested, we may not be able to perform that contract. In that case, we may have to stop providing a service to you. If so, we will notify you 
of this at the time.</p>

<h4>The bases on which we process information about you</h4>
<p>The law requires us to determine under which of six defined bases we process different categories of your personal information, and to notify
you of the basis for  each category. If a basis on which we process your personal information is no longer relevant then we shall immediately 
stop processing your data. If the basis changes then if required by law we shall notify you of the change and of any new basis under which we 
have determined that  we can continue to process your information.</p>

<h4>Information we process because we have a contractual obligation with you</h4>
<p>When you create an account on our website, buy a product or service from us, or otherwise agree to our terms and conditions, a contract is 
formed between you and us.In order to carry out our obligations under that contract we must process the information you give us. Some of
this information may be personal information.</p>

<h4>We may use it in order to:<h4>
<ul>
<li>verify your identity for security purposes</li>
<li>sell products to you</li>
<li>provide you with our services</li>
<li>provide you with suggestions and advice on products, services and how to obtain the most from using our website</li>
</ul>

<p>We process this information on the basis there is a contract between us, or that you have requested we use the information before we enter 
into a legal contract. We shall continue to process this information until the contract between us ends or is terminated by either party under
the terms of the contract.</p>

<h4>Information we process with your consent</h4>
<p>Through certain actions when otherwise there is no contractual relationship between us, such as when you browse our website or ask us to 
provide you more information about our business, including our products and services, you provide your consent to us to process information
that may be personal information.</p>
<p>Wherever possible, we aim to obtain your explicit consent to process this information, for example, by asking you to agree to our use of 
cookies. If you have given us explicit permission to do so, we may from time to time pass your name and contact information to selected 
associates whom we consider may provide services or products you would find useful. We continue to process your information on this basis 
until you withdraw your consent or it can be reasonably assumed that your consent no longer exists. You may withdraw your consent at any time
by instructing us readyfreddy.co.uk. However, if you do so, you may not be able to use our website or our services further.</p>

<h4>Information we process for the purposes of legitimate interests</h4>
<ul>
<li>We may process information on the basis there is a legitimate interest, either to you or to us, of doing so.</li>
<li>Where we process your information on this basis, we do after having given careful consideration to:</li>
<li>whether the same objective could be achieved through other means</li>
<li>whether processing (or not processing) might cause you harm</li>
<li>whether you would expect us to process your data, and whether you would, in the round, consider it reasonable to do so</li>
<li>For example, we may process your data on this basis for the purposes of:</li>
<li>record-keeping for the proper and necessary administration of our organisation</li>
<li>responding to unsolicited communication from you to which we believe you would expect a response</li>
<li>protecting and asserting the legal rights of any party</li>
<li>insuring against or obtaining professional advice that is required to manage organisational risk</li>
<li>protecting your interests where we believe we have a duty to do so</li>
</ul>

<h4>Information we process because we have a legal obligation</h4>
<p>Sometimes, we must process your information in order to comply with a statutory obligation.</p>
<p>For example, we may be required to give information to legal authorities if they so request or if they have the proper authorisation such as 
a search warrant or court order. This may include your personal information.</p>

