<p>It allows us to recognise visitors that you have referred to us, and to credit to you commission due for such referrals. It also includes information that allows us to transfer commission to you.The information is not used for any other purpose.</p>
<p>We undertake to preserve the confidentiality of the information and of the terms of our relationship. </p>
<p>We expect any affiliate or partner to agree to reciprocate this policy.</p>
<p>Use of information we collect through automated systems when you visit our website</p>

<h4>Cookies</h4>
<p>Cookies are small text files that are placed on your computer's hard drive by your web browser when you visit any website. They allow 
information gathered on one web page to be stored until it is needed for use on another, allowing a website to provide you with a personalised
experience and the website owner with statistics about how you use the website so that it can be improved. Some cookies may last for a 
defined period of time, such as one day or until you close your browser. </p>
<p>Others last indefinitely. Your web browser should allow you to delete any you choose. It also should allow you to prevent or limit their use.</p>

<p>Our website uses cookies. They are placed by software that operates on our servers, and by software operated by third parties whose services
we use. When you first visit our website, we ask you whether you wish us to use cookies. If you choose not to accept them, we shall not use 
them for your visit except to record that you have not consented to their use for any other purpose.
If you choose not to use cookies or you prevent their use through your browser settings, you will not be able to use all the functionality of our 
website.</p>

<h4>We use cookies in the following ways:</h4>
<p>to track how you use our website to record whether you have seen specific messages we display on our website to keep you signed in our 
site to record your answers to surveys and questionnaires on our site while you complete them to record the conversation thread during a live
chat with our support team.</p>

<h4>Personal identifiers from your browsing activity</h4>
<p>Requests by your web browser to our servers for web pages and other content on our website are recorded.</p>
<p>We record information such as your geographical location, your Internet service provider and your IP address. We also record information about 
the software you are using to browse our website, such as the type of computer or device and the screen resolution.</p>
<p>We use this information in aggregate to assess the popularity of the webpages on our website and how we perform in providing content to 
you. If combined with other information we know about you from previous visits, the data possibly could be used to identify you personally, 
even if you are not signed in to our website.</p>

<h4>Our use of re-marketing</h4>
<p>Re-marketing involves placing a cookie on your computer when you browse our website in order to be able to serve to you an advert for our 
products or services when you visit some other website. We may use a third party to provide us with re-marketing services from time to time. 
If so, then if you have consented to our use of cookies, you may see advertisements for our products and services on other websites.</p>

<p>Disclosure and sharing of your information</p>
<p>Information we obtain from third parties</p>
<p>Although we do not disclose your personal information to any third party (except as set out in this notice), we sometimes receive data that is 
indirectly made up from your personal information from third parties whose services we use. No such information is personally 
identifiable to you.</p>

