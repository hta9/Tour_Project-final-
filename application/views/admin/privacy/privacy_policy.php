<h4>Information relating to your method of payment</h4>
<p>We store information about your debit or credit card or other means of payment when you first provide it to us.</p>
<p>We store this payment information [at your request] in order to make repeat purchasing of goods and services easier next time you visit 
our website.</p>
<p>We also store it to help us prevent fraud.</p>

<h4>We take the following measures to protect your payment information:</h4>
<ul>
<li>We keep your payment information encrypted on our servers.</li>
<li>We do not keep all your payment information so as:</li>
<li>to prevent the possibility of our duplicating a transaction without a new instruction from you;</li>
<li>to prevent any other third party from carrying out a transaction without your consent</li>
<li>Access to your payment information is restricted to authorised staff only.</li>
<li>If we ask you questions about your payment information, we only show partial detail, so that you can identify the means of payment to 
which we refer.</li>
</ul>

